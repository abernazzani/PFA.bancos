    public void loadBandas()
    {
        BandasDAL bDAL = new BandasDAL();
        DefaultTableModel model = (DefaultTableModel)bandasTable.getModel();
        model.setRowCount(0);
        for(Banda entidad : bDAL.obtenerBandas())
        {
            model.addRow(new Object[] { entidad.getCodigoBanda(), entidad.getNroDeMiembros()});
        } 
    }
    
