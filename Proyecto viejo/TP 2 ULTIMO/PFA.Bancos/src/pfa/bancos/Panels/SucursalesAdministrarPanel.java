    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SucursalDAL sDAL = new SucursalDAL();
        String codigo = entidadesCombo.getSelectedItem().toString();
        ArrayList<Sucursal> sucursales = sDAL.getSucursalesPorCodigoEntidad(codigo);
        
        jTable1.removeAll();
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        if (jTable1.getRowCount() > 0) {
            for (int i = jTable1.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }
        
        for(Sucursal sucursal : sucursales)
        {
            model.addRow(new Object[] { sucursal.getCodigo(), sucursal.getDomicilio(), sucursal.getNroEmpleados() });
        }         
    }//GEN-LAST:event_jButton1ActionPerformed

