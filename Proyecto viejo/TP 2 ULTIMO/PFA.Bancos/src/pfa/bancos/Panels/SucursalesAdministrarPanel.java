    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        int rowIndex = jTable1.getSelectedRow();
        String codigo = jTable1.getValueAt(rowIndex, 0).toString();                    
        SucursalDAL sDAL = new SucursalDAL();
        SucursalesAddFrame saf = new SucursalesAddFrame(sDAL.getSucursalPorCodigo(codigo));
        saf.setVisible(true);
    }//GEN-LAST:event_editarBtnActionPerformed

    private void eliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBtnActionPerformed
        int rowIndex = jTable1.getSelectedRow();
        String codigo = jTable1.getValueAt(rowIndex, 0).toString();                     
        int reply = JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar la sucursal seleccionada?", "Sucursales", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION)
        {            
           SucursalDAL sDAL = new SucursalDAL();
           sDAL.eliminar(codigo);
        }
        jButton1.doClick(1);
    }//GEN-LAST:event_eliminarBtnActionPerformed

