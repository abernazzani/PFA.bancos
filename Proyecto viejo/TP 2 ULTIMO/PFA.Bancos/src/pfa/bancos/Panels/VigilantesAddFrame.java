    private void crearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearBtnActionPerformed
        
          if(usernameTextField.getText().length()!=0 && codigoTextField.getText().length()!=0 && 
                  nombreTextField.getText().length()!=0 && edadTextField.getText().length()!=0){
        
        UsuariosDAL uDAL =new UsuariosDAL();
        
        uDAL.crear(usernameTextField.getText(), new String(passwordField.getPassword()),3);
            
        VigilantesDAL vDAL = new VigilantesDAL();
        
        vDAL.crear((String)codigoTextField.getText(), (String)nombreTextField.getText(), 
                Integer.parseInt(edadTextField.getText()), (String)usernameTextField.getText());
        setVisible(false);
        dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Para crear o editar un vigilante debe completar los campos requeridos","Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_crearBtnActionPerformed

