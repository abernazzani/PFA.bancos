    private void loadAsaltos()
    {
        AsaltosDAL aDAL = new AsaltosDAL();
        DefaultTableModel model = (DefaultTableModel)asaltosTable.getModel();
        for(Asalto asalto : aDAL.getAsaltos())
        {
            model.addRow(new Object[] { asalto.getId(), asalto.getFecha(), asalto.getCodigoSucursal(), asalto.getCodigoJuez() });
        }
    }

