    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        AsaltosDAL aDAL = new AsaltosDAL();
        DefaultTableModel model = (DefaultTableModel)asaltosTable.getModel();
        
        int id =  Integer.parseInt(model.getValueAt(asaltosTable.getSelectedRow(), 0).toString());
        Asalto asalto = aDAL.getAsaltoPorId(id);
        AsaltosAddFrame aaf = new AsaltosAddFrame(asalto);
        aaf.setVisible(true);
    }//GEN-LAST:event_editarBtnActionPerformed

