    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        int rowIndex = jTable.getSelectedRow();
        String codigo = jTable.getValueAt(rowIndex, 0).toString();                    
        EntidadesBancariasDAL ebDAL = new EntidadesBancariasDAL();
        BancosAddFrame baf = new BancosAddFrame(ebDAL.obtenerEntidadPorCodigo(codigo));
        baf.setVisible(true);        
    }//GEN-LAST:event_editarBtnActionPerformed

    private void eliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBtnActionPerformed
        int rowIndex = jTable.getSelectedRow();
        String codigo = jTable.getValueAt(rowIndex, 0).toString();                     
        int reply = JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar la Entidad Bancaria seleccionada?", "Entidades Bancarias", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION)
        {            
           EntidadesBancariasDAL ebDAL = new EntidadesBancariasDAL();
           ebDAL.eliminar(codigo);
           initComponents();
           
        }     
    }//GEN-LAST:event_eliminarBtnActionPerformed


