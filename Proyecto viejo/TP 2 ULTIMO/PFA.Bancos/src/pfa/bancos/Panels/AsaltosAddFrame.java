    private void crearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearBtnActionPerformed
        
        try{
            SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd");
            java.util.Date fecha = df.parse(fechaTextField.getText()); 
        
        
        AsaltosDAL aDAL = new AsaltosDAL();
        DefaultTableModel model = (DefaultTableModel)delincuentesTable.getModel();
        ArrayList<String> delincuentes = new ArrayList<>();
        for(int i = 0; i < delincuentesTable.getRowCount(); i++)
        {
            delincuentes.add(model.getValueAt(i, 0).toString());
        }
        if("Guardar".equals(crearBtn.getText()))
        {
            aDAL.guardar(asaltoId, fechaTextField.getText(), JuecesCombo.getSelectedItem().toString()
                , sucursalesCombo.getSelectedItem().toString(), delincuentes);
        }
        else
        {
        aDAL.crear(fechaTextField.getText(), JuecesCombo.getSelectedItem().toString()
                , sucursalesCombo.getSelectedItem().toString(), delincuentes);        
        }
        
        if(condenasTable.getRowCount() > 0)
        {
            ArrayList<Condena> condenas = new ArrayList<>();            
            for(int i = 0; i < condenasTable.getRowCount(); i++)
            {                
                DateFormat format = new SimpleDateFormat("yyyy-mm-dd", Locale.ENGLISH);
                                
                String codigoDelincuente = condenasTable.getValueAt(i, 0).toString();
                String asalto = condenasTable.getValueAt(i, 0).toString();
                String fechaDeInicio = condenasTable.getValueAt(i, 0).toString();
                Date fInicioDate = new Date(2016, 10,14);
                try {
                     fInicioDate = (Date)format.parse(fechaDeInicio);
                } catch (ParseException ex) {
                    Logger.getLogger(AsaltosAddFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                String fechaFin = condenasTable.getValueAt(i, 0).toString();
                Date fFinDate = new Date(2016, 10,14);
                
                try {
                    fFinDate = (Date) format.parse(fechaDeInicio);
                } catch (ParseException ex) {
                    Logger.getLogger(AsaltosAddFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                                
                Condena condena = new Condena(codigoDelincuente, asaltoId,
                        fInicioDate, fFinDate);
                
            }
        }       
        setVisible(false);
        dispose();
        } catch (ParseException ex) {
            
            JOptionPane.showMessageDialog(this, "Error en el campo fecha, debe tener la siguiente estructura 'yyyy/mm/dd'","Error", JOptionPane.ERROR_MESSAGE);
            
        }
        
        
        
    }//GEN-LAST:event_crearBtnActionPerformed

