    public void loadDelincuentes()
    {
        DelincuentesDAL dDAL = new DelincuentesDAL();        
        ArrayList<Delincuente> delincuentes = dDAL.getDelincuentes();
        delincuentesCombo.removeAllItems();
        delincuentesCombo.addItem("Seleccione Delincuente");
        for(Delincuente delincuente : delincuentes)
        {
            Boolean agregarDelincuente = true;
            DefaultTableModel model = (DefaultTableModel)delincuentesTable.getModel();            
            for(int i = 0; i < delincuentesTable.getRowCount(); i++)
            {
                if(model.getValueAt(i, 0).toString().equals(delincuente.getCodigo()))
                {
                    agregarDelincuente = false;
                    break;
                }
            }
            if(agregarDelincuente)
            {
                delincuentesCombo.addItem(delincuente.getCodigo());
            }
        }
        agregarCondenaBtn.setEnabled(false);
        quitarCondenaBtn.setEnabled(false);
    }
    
    public AsaltosAddFrame(Asalto asalto)
    {
        this();
        Image icono = Toolkit.getDefaultToolkit().getImage("pfaIcon.png");
        this.setIconImage(icono);     
        setLocationRelativeTo(null);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        
        
        JuecesCombo.setSelectedItem(asalto.getCodigoJuez());
        sucursalesCombo.setSelectedItem(asalto.getCodigoSucursal());
        
        fechaTextField.setText(asalto.getFecha().toString());
        
        asaltoId = asalto.getId();
        
        DelincuentesDAL dDAL = new DelincuentesDAL();
        
        for(Delincuente delincuente : dDAL.getPorAsalto(asalto.getId()))
        {
            DefaultTableModel model = (DefaultTableModel)delincuentesTable.getModel();            
            model.addRow(new Object[] { delincuente.getCodigo(), delincuente.getNombre(), delincuente.getCodigoBanda(), delincuente.getDetenido()});
        }
        
        CondenasDAL cDAL = new CondenasDAL();
        
        for(Condena condena : cDAL.getCondenasPorAsalto(asalto.getId()))
        {
            DefaultTableModel model = (DefaultTableModel)condenasTable.getModel();            
            model.addRow(new Object[] { condena.getCodigoDelDelincuente(), condena.getFechaDeInicio(), condena.getFechaFin()});
        }
        
        crearBtn.setText("Guardar");
        agregarCondenaBtn.setEnabled(true);
        quitarCondenaBtn.setEnabled(true);
    }

