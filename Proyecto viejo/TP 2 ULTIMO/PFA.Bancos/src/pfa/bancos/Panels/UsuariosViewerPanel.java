    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
       
        int rowIndex = jTable.getSelectedRow();
        int id = (int) jTable.getValueAt(rowIndex, 2);                   
        UsuariosDAL usDAL = new UsuariosDAL();        
        AdminInvestADDPanel usPL = new AdminInvestADDPanel(usDAL.getObtenerUsuarioPorID(id));
        
        jPanel1.removeAll();
        jPanel1.setLayout(new BorderLayout());
        jPanel1.add(usPL, BorderLayout.CENTER);
        jPanel1.revalidate(); 
        
        //jPanel1.setVisible(true);
        
        //usPL.setVisible(true);        
    }//GEN-LAST:event_editarBtnActionPerformed

    private void eliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBtnActionPerformed
        int rowIndex = jTable.getSelectedRow();
        int codigo = (int) jTable.getValueAt(rowIndex, 2);                     
        int reply = JOptionPane.showConfirmDialog(null, "多Desea eliminar el usuario seleccionado?", "Usuarios", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION)
        {            
           UsuariosDAL usDAL = new UsuariosDAL();
           usDAL.eliminar(codigo);
                       
        }     
    }//GEN-LAST:event_eliminarBtnActionPerformed
    
