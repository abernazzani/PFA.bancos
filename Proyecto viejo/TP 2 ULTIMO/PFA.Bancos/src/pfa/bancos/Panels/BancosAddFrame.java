    private void crearBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearBtnActionPerformed
        
        if(codigoBancoTextField.getText().length()!=0 &&  nombreTextField.getText().length()!=0 && direccionTextField.getText().length()!=0){
            
            EntidadesBancariasDAL ebDAL = new EntidadesBancariasDAL();
            
            if(!"Guardar".equals(crearBtn.getText()))
            {
                ebDAL.crearEntidadBancaria(codigoBancoTextField.getText(), nombreTextField.getText(), direccionTextField.getText());
            }
            else
            {
                ebDAL.editarEntidadBancaria(codigoBancoTextField.getText(), nombreTextField.getText(), direccionTextField.getText());
            }
            setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Para crear o editar una entidad debe completar los campos requeridos","Error", JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_crearBtnActionPerformed
        
