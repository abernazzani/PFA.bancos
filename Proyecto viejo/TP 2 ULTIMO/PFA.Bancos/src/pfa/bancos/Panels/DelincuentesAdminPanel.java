    public void loadDelincuentes()
    {
        DelincuentesDAL dDAL = new DelincuentesDAL();
        DefaultTableModel model = (DefaultTableModel)delincuentesTable.getModel();
        model.setRowCount(0);
        for(Delincuente entidad : dDAL.getDelincuentes())
        {
            model.addRow(new Object[] { entidad.getCodigo(), entidad.getNombre(), (entidad.getDetenido() ? "Sí" : "No"), entidad.getCodigoBanda() });
        } 
    }

