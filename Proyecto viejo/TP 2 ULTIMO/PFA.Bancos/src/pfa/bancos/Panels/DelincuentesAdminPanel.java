    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        int rowIndex = delincuentesTable.getSelectedRow();
        String codigo = delincuentesTable.getValueAt(rowIndex, 0).toString();
        DelincuentesDAL dDAL = new DelincuentesDAL();
        DelincuentesAddFrame saf = new DelincuentesAddFrame(dDAL.getPorCodigo(codigo));
        saf.setVisible(true);
    }//GEN-LAST:event_editarBtnActionPerformed

    private void eliminarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarBtnActionPerformed
        int rowIndex = delincuentesTable.getSelectedRow();
        String codigo = delincuentesTable.getValueAt(rowIndex, 0).toString();
        int reply = JOptionPane.showConfirmDialog(null, "¿Desea eliminar la sucursal seleccionada?", "Sucursales", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION)
        {
            DelincuentesDAL sDAL = new DelincuentesDAL();
            sDAL.eliminar(codigo);
            loadDelincuentes();
        }
    }//GEN-LAST:event_eliminarBtnActionPerformed


